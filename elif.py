# The elif (else if) Statement
# Use elif statement to specify a new condition to test, if the first
# condition is false.
x, y = 5, 5
if x > y:
    print('x is greater')
elif x < y:
    print('y is greater')
else:
    print('x and y are equal')

# Prints
# x and y are equal
